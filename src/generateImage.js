import OpenAI from "openai";
import fs from "fs";
import path from "path";
import { v4 as uuidv4 } from "uuid";
// Stick to the standard library you already have installed
import { GoogleGenerativeAI } from "@google/generative-ai";

const openai = new OpenAI({
  apiKey: process.env.OPENAI_API_KEY
});

const genAI = new GoogleGenerativeAI(process.env.GEMINI_API_KEY);

export async function generateImage(post) {
  const prompt = `Soft hand-drawn minimal illustration of a ${post.breed}, ${post.furColor} fur color. Scene: ${post.imagePrompt}. Light neutral background, 1080x1080 square, no text.`;

  const fileName = `${uuidv4()}.png`;
  const filePath = path.join("/tmp", fileName);

  // 1Ô∏è‚É£ TRY OPENAI FIRST
  try {
    console.log("üñº Trying OpenAI (DALL-E 3)...");
    const response = await openai.images.generate({
      model: "dall-e-3",
      prompt,
      size: "1024x1024",
      response_format: "b64_json"
    });

    const imageBase64 = response.data[0].b64_json;
    fs.writeFileSync(filePath, Buffer.from(imageBase64, "base64"));
    return { imagePath: filePath, provider: "OpenAI" };

  } catch (openaiError) {
    console.log(`‚ö†Ô∏è OpenAI failed: ${openaiError.message}. Switching to Gemini...`);

    // 2Ô∏è‚É£ FALLBACK TO GEMINI (Using Imagen 3 Model)
    try {
      // Use the stable Imagen 3 model name
      const model = genAI.getGenerativeModel({ model: "imagen-3.0-generate-001" });

      // In the standard SDK, we use generateContent with IMAGE modality
      const result = await model.generateContent({
        contents: [{ role: "user", parts: [{ text: prompt }] }],
        generationConfig: {
          responseModalities: ["IMAGE"],
        },
      });

      const imagePart = result.response.candidates[0].content.parts.find(
        part => part.inlineData
      );

      if (!imagePart) throw new Error("No image data returned from Google.");

      const imageBase64 = imagePart.inlineData.data;
      fs.writeFileSync(filePath, Buffer.from(imageBase64, "base64"));

      console.log("‚úÖ Success: Image generated by Google Imagen.");
      return { imagePath: filePath, provider: "Google" };

    } catch (googleError) {
      console.error("‚ùå All providers failed.");
      console.error("Google Error:", googleError.message);
      throw googleError;
    }
  }
}
