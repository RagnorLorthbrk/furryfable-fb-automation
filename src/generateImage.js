import OpenAI from "openai";
import fs from "fs";
import path from "path";
import { v4 as uuidv4 } from "uuid";
import { GoogleGenerativeAI } from "@google/generative-ai";

const openai = new OpenAI({
  apiKey: process.env.OPENAI_API_KEY
});

const genAI = new GoogleGenerativeAI(process.env.GEMINI_API_KEY);

export async function generateImage(post) {

  const prompt = `Soft hand-drawn minimal illustration of a ${post.breed}, ${post.furColor} fur color. Scene: ${post.imagePrompt}. Light neutral background, calm storytelling mood, 1080x1080 square, no text, no watermark.`;

  const fileName = `${uuidv4()}.png`;
  const filePath = path.join("/tmp", fileName);

  // 1Ô∏è‚É£ TRY OPENAI FIRST
  try {
    console.log("üñº Trying OpenAI (DALL-E 3)...");
    const response = await openai.images.generate({
      model: "dall-e-3",
      prompt,
      size: "1024x1024",
      response_format: "b64_json"
    });

    const imageBase64 = response.data[0].b64_json;
    fs.writeFileSync(filePath, Buffer.from(imageBase64, "base64"));
    console.log("‚úÖ Success: Image generated by OpenAI.");
    return { imagePath: filePath, provider: "OpenAI" };

  } catch (openaiError) {
    console.log(`‚ö†Ô∏è OpenAI failed (${openaiError.message}). Switching to Gemini 2.0...`);

    // 2Ô∏è‚É£ FALLBACK TO GEMINI 2.0 FLASH
    try {
      // Use the newest 2.0 Flash model
      const model = genAI.getGenerativeModel({ model: "gemini-2.0-flash" });

      const result = await model.generateContent({
        contents: [{ role: "user", parts: [{ text: prompt }] }],
        generationConfig: {
          // This is the key: it tells Gemini to output an image, not text
          responseModalities: ["IMAGE"],
        },
      });

      const response = result.response;
      const candidate = response.candidates[0];
      
      // Find the part that contains the image data
      const imagePart = candidate.content.parts.find(part => part.inlineData);

      if (!imagePart) {
        throw new Error("Gemini did not return an image. It might have returned text or blocked the prompt due to safety filters.");
      }

      const imageBase64 = imagePart.inlineData.data;
      fs.writeFileSync(filePath, Buffer.from(imageBase64, "base64"));

      console.log("‚úÖ Success: Image generated by Gemini.");
      return { imagePath: filePath, provider: "Gemini" };

    } catch (geminiError) {
      console.error("‚ùå Both providers failed.");
      console.error("Gemini Details:", geminiError.message);
      throw geminiError;
    }
  }
}
