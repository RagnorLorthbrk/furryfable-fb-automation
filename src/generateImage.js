import OpenAI from "openai";
import fs from "fs";
import path from "path";
import { v4 as uuidv4 } from "uuid";
import { GoogleGenerativeAI } from "@google/generative-ai";

const openai = new OpenAI({
  apiKey: process.env.OPENAI_API_KEY
});

// Initialize Gemini with your API Key
const genAI = new GoogleGenerativeAI(process.env.GEMINI_API_KEY);

export async function generateImage(post) {

  const prompt = `
Soft hand-drawn minimal illustration.
Light neutral background.
Calm emotional storytelling mood.
Dog breed: ${post.breed}
Fur color: ${post.furColor}
Scene: ${post.imagePrompt}
1080x1080 square.
No text.
No watermark.
`;

  const fileName = `${uuidv4()}.png`;
  const filePath = path.join("/tmp", fileName);

  // 1Ô∏è‚É£ TRY OPENAI FIRST
  try {
    console.log("üñº Trying OpenAI image generation (DALL-E 3)...");

    const response = await openai.images.generate({
      model: "dall-e-3", // Updated from "gpt-image-1" to the correct stable model name
      prompt,
      size: "1024x1024",
      response_format: "b64_json"
    });

    const imageBase64 = response.data[0].b64_json;
    fs.writeFileSync(filePath, Buffer.from(imageBase64, "base64"));

    console.log("‚úÖ Success: Image generated by OpenAI.");
    return { imagePath: filePath, provider: "OpenAI" };

  } catch (openaiError) {
    console.log(`‚ö†Ô∏è OpenAI failed (${openaiError.message}). Switching to Gemini...`);

    // 2Ô∏è‚É£ FALLBACK TO GEMINI
    try {
      // Using the stable 'gemini-2.0-flash' model
      const model = genAI.getGenerativeModel({
        model: "gemini-2.0-flash"
      });

      const result = await model.generateContent({
        contents: [
          {
            role: "user",
            parts: [{ text: prompt }]
          }
        ],
        generationConfig: {
          // Requesting direct image output modality
          responseModalities: ["IMAGE"]
        }
      });

      // Find the image part in the response candidates
      const imagePart = result.response?.candidates?.[0]?.content?.parts?.find(
        part => part.inlineData
      );

      if (!imagePart || !imagePart.inlineData) {
        throw new Error("Gemini returned a response, but no image data was found.");
      }

      const imageBase64 = imagePart.inlineData.data;
      fs.writeFileSync(filePath, Buffer.from(imageBase64, "base64"));

      console.log("‚úÖ Success: Image generated by Gemini.");
      return { imagePath: filePath, provider: "Gemini" };

    } catch (geminiError) {
      console.error("‚ùå Both OpenAI and Gemini image generation failed.");
      console.error("Gemini Error Details:", geminiError.message);
      throw geminiError;
    }
  }
}
