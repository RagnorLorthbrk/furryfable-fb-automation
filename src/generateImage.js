import OpenAI from "openai";
import fs from "fs";
import path from "path";
import { v4 as uuidv4 } from "uuid";
import axios from "axios";

const openai = new OpenAI({ apiKey: process.env.OPENAI_API_KEY });
const GEMINI_API_KEY = process.env.GEMINI_API_KEY;

export async function generateImage(post) {
  // Brand prompt formula for FurryFable
  const prompt = `Professional studio pet photography, soft bokeh, natural lighting. A high-quality image of a ${post.breed}, ${post.furColor} fur color. Scene: ${post.imagePrompt}. Ensure the pet looks happy and healthy. No distorted limbs, extra tails, or text.`;

  const fileName = `${uuidv4()}.png`;
  const filePath = path.join("/tmp", fileName);

  // 1Ô∏è‚É£ TRY OPENAI FIRST (DALL-E 3)
  try {
    console.log("üñº Trying OpenAI (DALL-E 3)...");
    const response = await openai.images.generate({
      model: "dall-e-3",
      prompt,
      size: "1024x1024",
      response_format: "b64_json"
    });
    fs.writeFileSync(filePath, Buffer.from(response.data[0].b64_json, "base64"));
    console.log("‚úÖ Success: Image generated by OpenAI.");
    return { imagePath: filePath, provider: "OpenAI" };

  } catch (openaiError) {
    console.log(`‚ö†Ô∏è OpenAI failed (${openaiError.message}). Switching to Gemini (REST)...`);

    // 2Ô∏è‚É£ FALLBACK TO GEMINI (Matching your Blog Script logic)
    try {
      // Use the stable 2.5 Flash Image model
      const url = `https://generativelanguage.googleapis.com/v1beta/models/gemini-2.5-flash-image:generateContent?key=${GEMINI_API_KEY}`;
      
      const res = await axios.post(url, {
        contents: [{ 
          parts: [{ text: prompt }] 
        }],
        generationConfig: {
          responseModalities: ["IMAGE"] // Forces image output
        }
      });

      const imageData = res.data.candidates[0].content.parts.find(p => p.inlineData)?.inlineData.data;
      
      if (!imageData) throw new Error("No image data returned from Gemini.");

      fs.writeFileSync(filePath, Buffer.from(imageData, "base64"));
      console.log("‚úÖ Success: Image generated by Gemini.");
      return { imagePath: filePath, provider: "Gemini" };

    } catch (geminiError) {
      console.error("‚ùå All image providers failed.");
      console.error("Gemini Details:", geminiError.message);
      throw geminiError;
    }
  }
}
